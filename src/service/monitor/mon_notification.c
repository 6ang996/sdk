U32 mon_send_email(S8 * pszMsg, S8 * pszEmailAddress)
{
    S8 szBuffCmd[1024] = {0};
    FILE * fp = NULL;

    dos_snprintf(szBuffCmd, sizeof(szBuffCmd), "echo %s | mail -s %s %s", pszMsg, pszTitle, pszEmailAddress);
    fp = popen(szBuffCmd, "r");
    if (DOS_ADDR_INVALID(fp))
    {
        DOS_ASSERT(0);
        return DOS_FAIL;
    }

    pclose(fp);
    fp = NULL;
    return DOS_SUCC;
}

