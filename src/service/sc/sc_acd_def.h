#ifndef __SC_ACD_PUB_H__
#define __SC_ACD_PUB_H__

/* 单个坐席最大可以属于组的个数 */
#define MAX_GROUP_PER_SITE      2

/* IP坐席:
 * 1. 初始化为OFFLINE状态
 * 2. 登陆之后就处于IDEL状态
 * 3. 签入之后就进入CONNECTED状态
 * 4. 签出之后就处于IEDL状态
 * 5. WEB上点击离开就处于离线状态
 * 6. 接通呼叫就处于BUSY状态
 * 7. 呼叫结束后如果坐席没有挂机就进入CONNECTED状态，坐席挂机了，就处于IDEL状态
 * PSTN坐席:
 * 1.初始化为SC_ACD_IDEL状态
 * 2.呼叫中为BUSY状态
 * 3.呼叫结束后如果坐席没有挂机就进入CONNECTED状态，坐席挂机了，就处于IDEL状态
 */
enum {
    SC_ACD_OFFLINE = 0,                 /* 离线状态，不可用 */
    SC_ACD_IDEL,                        /* 坐席已可用 */
    SC_ACD_CONNECTED,                   /* 坐席已经签入 */
    SC_ACD_AWAY,                        /* 坐席暂时离开，不可用 */
    SC_ACD_BUSY,                        /* 坐席忙，不可用 */

    SC_ACD_BUTT
};

enum {
    SC_ACD_SITE_ACTION_DELETE = 0,       /* 坐席操作动作，删除 */
    SC_ACD_SITE_ACTION_ADD,              /* 坐席操作动作，删除 */
    SC_ACD_SITE_ACTION_UPDATE,           /* 坐席操作动作，删除 */
    SC_ACD_SITE_ACTION_SIGNIN,           /* 坐席登陆，  */
    SC_ACD_SITE_ACTION_SIGNOUT,          /* 坐席登出 */
    SC_ACD_SITE_ACTION_ONLINE,           /* 坐席置闲 */
    SC_ACD_SITE_ACTION_OFFLINE,          /* 坐席置忙 */
    SC_ACD_SITE_ACTION_EN_QUEUE,         /* 坐席长连 */
    SC_ACD_SITE_ACTION_DN_QUEUE,         /* 坐席退出长连 */

    SC_ACD_SITE_ACTION_BUTT              /* 坐席签出(长连) */
};

enum {
    SC_ACD_POLICY_INCREASE               /* 增加 */,
    SC_ACD_POLICY_INCREASE_LOOP,         /* 循环增加 */
    SC_ACD_POLICY_REDUCTION,             /* 递减 */
    SC_ACD_POLICY_REDUCTION_LOOP,        /* 循环递减 */
    SC_ACD_POLICY_MIN_CALL,

    SC_ACD_POLICY_BUTT
};

typedef struct tagACDSiteDesc{
    U16        usSCBNo;
    U16        usStatus;                          /* 坐席状态 refer to SC_SITE_STATUS_EN */
    U32        ulSiteID;                          /* 坐席数据库编号 */
    U32        ulCallCnt;                         /* 呼叫总数 */
    U32        ulCustomerID;                      /* 呼叫总数 */
    U32        aulGroupID[MAX_GROUP_PER_SITE];    /* 组ID */

    U32        bValid:1;                          /* 是否可用 */
    U32        bRecord:1;                         /* 是否录音 */
    U32        bTraceON:1;                        /* 是否调试跟踪 */
    U32        bAllowAccompanying:1;              /* 是否允许分机随行 refer to SC_SITE_ACCOM_STATUS_EN */
    U32        bGroupHeader:1;                    /* 是否是组长 */
    U32        bLogin:1;                          /* 是否已经登录 */
    U32        bConnected:1;                      /* 是否已经长连 */
    U32        bWaitingDelete:1;                  /* 是否是组长 */
    U32        ucRes1:24;

    S8         szUserID[SC_TEL_NUMBER_LENGTH];    /* SIP User ID */
    S8         szExtension[SC_TEL_NUMBER_LENGTH]; /* 分机号 */
    S8         szEmpNo[SC_EMP_NUMBER_LENGTH];     /* 工号 */

    pthread_mutex_t  mutexLock;
}SC_ACD_SITE_DESC_ST;

U32 sc_acd_init();
SC_ACD_SITE_DESC_ST  *sc_acd_get_site_by_grpid(U32 ulGroupID);
U32 sc_acd_agent_update_status(SC_ACD_SITE_DESC_ST *pstAgent, U32 ulStatus);

#endif

